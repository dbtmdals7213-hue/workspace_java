
public class Opr02 {

	public static void main(String[] args) {
		
		int a = 10, b = 4;
		
		boolean test;	// 비교 연산의 결과 값인 true 또는 false 가 향후에 저장될 변수 선언
		
		// 10이 4보다 더 크냐? 라고 묻는 비교 연산식 작성하여 컴퓨터에게 물어보면? 컴퓨터는 a > b 식으로 해석해서 true 값을 돌려준다.
		test = a > b;
		System.out.println(a + " > " + b + " = " + test);
		

	}

}
